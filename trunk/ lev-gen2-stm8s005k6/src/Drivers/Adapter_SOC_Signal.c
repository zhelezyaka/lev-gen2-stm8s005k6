void _Device_Init_Adapter_Output_Signal_Pin_Function(void){
	// ADP Soc
	GPIO_Init(ADP_SOC_PORT, ADP_SOC_PIN, GPIO_MODE_OUT_PP_LOW_FAST);
}

void _Device_Set_ADP_SOC(unsigned char enable){
  if(enable){
    //DeviceOn
    GPIO_WriteHigh(ADP_SOC_PORT, ADP_SOC_PIN);
  }else{
    //DeviceOff
    GPIO_WriteLow(ADP_SOC_PORT, ADP_SOC_PIN);
  }
}
